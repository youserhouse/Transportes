const PRT_KG_BREAKS = [10,20,24,30,40,50,60,70,80,90,100,200,300,400,500,1000,1500,2000];

// Palletways Portugal zone logic
function getPwZonaPrt(cpStr) {
  const cp = parseInt(cpStr);
  const cp4 = cpStr.length >= 4 ? parseInt(cpStr.substring(0,4)) : null;
  // Zone 7: 10-19, 26-29, 40-45
  if ((cp>=10&&cp<=19)||(cp>=26&&cp<=29)||(cp>=40&&cp<=45)) return 7;
  // Zone 7: 3860-3899 (needs 4 digits)
  if (cp===38) {
    if (cp4!==null && cp4>=3860 && cp4<=3899) return 7;
    return 8; // default to zone 8, disclaimer shown
  }
  return 8;
}

function calcCevaPrt(numPalets, alturaTotal, cpStr) {
  const cp2 = parseInt(cpStr.substring(0,2));
  const tarifa = CEVA_PRT[cp2];
  if (!tarifa) return null;

  // Build weight
  const totalKg = Math.round(alturaTotal * 250);
  let palets = [], remaining = alturaTotal;
  for (let i=0;i<numPalets;i++) {
    if (i === numPalets - 1) {
      const kg = remaining > 0 ? Math.round(remaining*250) : 250;
      palets.push({altura:remaining>0?remaining:1.0, kg, full:remaining>=1.0});
      remaining = 0;
    } else if(remaining>=1.0){palets.push({altura:1.0,kg:250,full:true});remaining=Math.round((remaining-1.0)*100)/100;}
    else if(remaining>0){const kg=Math.round(remaining*250);palets.push({altura:remaining,kg,full:false});remaining=0;}
    else{palets.push({altura:1.0,kg:250,full:true});}
  }

  // Find bracket index
  let idx = PRT_KG_BREAKS.findIndex(b => totalKg <= b);
  if (idx === -1) idx = PRT_KG_BREAKS.length - 1;

  const tableVal = tarifa[idx];
  const kgBreak = PRT_KG_BREAKS[idx];

  // For ranges >= 200kg: price = totalKg × (tableVal/100)
  // For ranges <= 100kg: fixed price
  let basePrice;
  let rangeLabel;
  if (kgBreak <= 100) {
    basePrice = tableVal;
    rangeLabel = `≤${kgBreak} kg (precio fijo)`;
  } else {
    basePrice = totalKg * (tableVal / 100);
    rangeLabel = `${totalKg} kg × ${(tableVal/100).toFixed(4)}€/kg (tabla ${kgBreak})`;
  }

  const surcharge = basePrice * CONFIG.CEVA_RECARGO_PCT;
  return { total: basePrice+surcharge, basePrice, surcharge, totalKg, rangeLabel, palets, cp2 };
}

